#Virtual environment

"""
To create virtual environment we can type:
- python3 -m venv (Virtual env name)
in terminal

To use a certain virtual environment we can do:
- source (Virtual env name)/bin/activate

To come out of the virtual environment we can:
- deactivate

To check what are all the packages installed in the virtual env:
- pip3 freeze

To give the output into the requirements file:
- pip3 freeze > requirements.xt

To install all the modules or packages in the requirements.txt:
- pip3 install -r requirements.txt
"""